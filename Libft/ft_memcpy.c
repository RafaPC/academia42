void	*ft_memcpy(void *dst, const void *src, unsigned int n)
{
	unsigned char	*dest;
	unsigned char	*source;
	int				i;

	i = 0;
	source = (unsigned char *)src;
	dest = (unsigned char *)dst;
	while (i < n)
	{
		dest[i] = source[i];
		i++;
	}
	return (dst);
}
